function PrecacheUtils:_PrecacheList(list, type, context)
    for _, path in ipairs(list) do
        if type == "item" then
            PrecacheItemByNameSync(path, context)
        elseif type == "unit" then
            PrecacheUnitByNameSync(path, context)
        elseif type == "particle_folder" then
            PrecacheResource("particle_folder", path, context)
        else
            PrecacheResource(type, path, context)
        end
    end
end

function PrecacheUtils.Precache(context)
    PrecacheUtils:_PrecacheList(PrecacheUtils.items,     "item",            context)
    PrecacheUtils:_PrecacheList(PrecacheUtils.units,     "unit",            context)
    PrecacheUtils:_PrecacheList(PrecacheUtils.sounds,    "soundfile",       context)
    PrecacheUtils:_PrecacheList(PrecacheUtils.particles, "particle",        context)
    PrecacheUtils:_PrecacheList(PrecacheUtils.particle_folders, "particle_folder", context)
    PrecacheUtils:_PrecacheList(PrecacheUtils.models,    "model",           context)
    local map = GetMapName()
    local cfg = PrecacheUtils.mapResources[map]
    if cfg then
        PrecacheUtils:_PrecacheList(cfg.particles or {}, "particle", context)
        PrecacheUtils:_PrecacheList(cfg.models or {},    "model",    context)
    end
end

return PrecacheUtils

